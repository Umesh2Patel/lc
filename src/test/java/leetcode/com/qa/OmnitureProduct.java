package leetcode.com.qa;

public class OmnitureProduct {
	
	int product;
	String events;
	String eVars;
		
	public OmnitureProduct(int product, String events, String eVars) {
		super();
		this.product = product;
		this.events = events;
		this.eVars = eVars;
	}

	public int getProduct() {
		return product;
	}
	public void setProduct(int product) {
		this.product = product;
	}
	public String getEvents() {
		return events;
	}
	public void setEvents(String events) {
		this.events = events;
	}
	public String geteVars() {
		return eVars;
	}
	public void seteVars(String eVars) {
		this.eVars = eVars;
	}
	
	
	
	

}
